# Sum (2 + 3) = 5
print(2 + 3)

# Difference (10 - 7) = 3
print(10 - 7)

# Multiplication (5 * 4) = 20
print(5 * 4)

# Division (Quotient) (10 / 4) = 2.5
print(10 / 4)

# Division (Remainder) a.k.a Mod (20 % 7) = 6
print(20 % 7)

# Floor is a mathematical function which returns the least integral value of a fraction.
# Floor(1.25) = 1 {1.25 comes between 1 and 2, so least integral value is 1}
# Floor(-2.75) = -3 {-2.75 comes between -2 and -3, so least integral value is -3}
# Floor Division -> Floor(10 / 7) = 1 | Floor(-25 / 4) = -7
print(10 // 7)
print(-25 // 4)

# Power (3 ^ 3) = 27
print(3 ** 3)

# Square Root of 25
print(25 ** 0.5)
